#!/bin/bash
#Сравнивает строки между двумя файлами
#Ключи(2): пути к двум файлам для сравнения

#Файлы для сравнения
name1=$1
name2=$2

str1="$(tr '\n' ' ' < "$name1"  |  tr '\0' ' ' | sed 's/^[ \t]*//;s/[ \t]*$//' )"
str2="$(tr '\n' ' ' < "$name2"  |  tr '\0' ' ' | sed 's/^[ \t]*//;s/[ \t]*$//' )"

if [ "$str1" == "$str2" ]; then
    exit "0" #Пройдено
else
    exit "1" #Не пройдено
fi
